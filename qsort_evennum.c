#include <stdio.h>
#include <stdlib.h>

// Компаратор для сортировки целых чисел по возрастанию
int compare_ints(const void *a, const void *b) {
    int x = *(const int*)a;  // Получаем первое число
    int y = *(const int*)b;  // Получаем второе число
    
    // Умный трюк для возврата -1, 0 или 1 без if
    // (x > y) - (x < y) даёт:
    // - если x < y: (0) - (1) = -1
    // - если x > y: (1) - (0) = 1  
    // - если x = y: (0) - (0) = 0
    return (x > y) - (x < y);
}

int main() {
    int N;  // Количество чисел
    scanf("%d", &N);
    
    // Объявляем массивы (по условию N ≤ 1000)
    int arr[1000];          // Исходный массив всех чисел
    int evens[1000];        // Только чётные числа из arr
    int positions[1000];    // Позиции чётных чисел в arr
    int even_count = 0;     // Счётчик чётных чисел
    
    // 1. ЧТЕНИЕ ДАННЫХ
    // Читаем все числа и собираем информацию о чётных
    for (int i = 0; i < N; i++) {
        scanf("%d", &arr[i]);  // Читаем очередное число
        
        // Проверяем, чётное ли число
        // (число чётное, если остаток от деления на 2 равен 0)
        if (arr[i] % 2 == 0) {
            // Сохраняем значение чётного числа
            evens[even_count] = arr[i];
            // Сохраняем его позицию в исходном массиве
            positions[even_count] = i;
            // Увеличиваем счётчик чётных чисел
            even_count++;
        }
    }
    
    // 2. СОРТИРОВКА ЧЁТНЫХ ЧИСЕЛ
    // Сортируем массив evens по возрастанию
    // qsort(массив, количество_элементов, размер_элемента, функция_сравнения)
    qsort(evens, even_count, sizeof(int), compare_ints);
    
    // 3. СОРТИРОВКА ПОЗИЦИЙ
    // Сортируем массив positions по возрастанию
    // Это нужно, чтобы расставить отсортированные числа
    // в порядке следования их позиций в исходном массиве
    qsort(positions, even_count, sizeof(int), compare_ints);
    
    // 4. ВСТАВКА ОТСОРТИРОВАННЫХ ЧЁТНЫХ ЧИСЕЛ
    // Расставляем отсортированные чётные числа на их места
    for (int i = 0; i < even_count; i++) {
        // Берём i-е отсортированное чётное число
        // и помещаем его на i-ю позицию среди чётных чисел
        // (позиции отсортированы по возрастанию)
        arr[positions[i]] = evens[i];
    }
    
    // 5. ВЫВОД РЕЗУЛЬТАТА
    // Выводим весь массив, где чётные отсортированы,
    // а нечётные остались на своих местах
    for (int i = 0; i < N; i++) {
        printf("%d ", arr[i]);
    }
    printf("\n");
    
    return 0;
}